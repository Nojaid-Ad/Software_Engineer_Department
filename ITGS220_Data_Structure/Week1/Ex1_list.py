# Example of an array 
List = [10, 20, 30, 40, 50]



# Accessing elements by index
print(List[0])  # Output: 10
print(List[2])  # Output: 30

# Modifying elements
List[1] = 25
print(List)  # Output: [10, 25, 30, 40, 50]

# Iterating through elements
for element in List:
    print(element)
